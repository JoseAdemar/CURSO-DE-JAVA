package fundamentos;

public class Wrapper {

	 public static void main(String[] args) {
		
		 //byte primitivo
		 Byte b = 10; //wrapper
		 System.out.println(b);
		 
		 //short primitivo
		 Short s = 20; //wrapper
		 System.out.println(s);
		 
		 //int primitivo
		 Integer i = 50; //wrapper
		 System.out.println(i);
		 
		 //long primitivo
		 Long l = 100L; //wrapper
		 System.out.println(l);
		 
		 //boolean primitivo
		 Boolean bo = true; //wrapper
		 System.out.println(bo);
		 
		 //float primitivo
		 Float f = 10.10F; //wrapper
		 System.out.println(f);
		 
		 //double primitivo
		 Double d = 100.50; //wrapper
		 System.out.println(d);
		 
		 //char primitivo
		 Character ch = 'M'; //wrapper
		 System.out.println(ch);
	}
}
